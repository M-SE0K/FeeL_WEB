function parseTSV(tsv) {
  const phoneRe = /^(0\d{1,3}-\d{3,4}-\d{4}|01\d-\d{3,4}-\d{4})$/;
  const lines = tsv
    .split(/\r?\n/)
    .map(l => l.trim())
    .filter(Boolean);
  const rows = [];
  for (const line of lines) {
    const cols = line.split(/\t+/); // 탭 기준 분리
    if (!cols.length) continue;
    // 헤더 행 스킵
    if (cols[0] === '업체명') continue;

    if (cols.length >= 4) {
      const [name, address, phone, benefitsRaw] = cols;
      rows.push({
        name,
        address,
        phone,
        benefits: (benefitsRaw || '')
          .split(/\s*[/•\-]\s*|\s{2,}/)
          .map(s => s.trim())
          .filter(Boolean),
        lat: null,
        lng: null,
      });
      continue;
    }

    // 특수 케이스: 3컬럼이며 2,3번째가 전화번호 형태(후토루)
    if (cols.length === 3 && phoneRe.test(cols[1]) && phoneRe.test(cols[2])) {
      const [name, phone, otherPhone] = cols;
      rows.push({
        name,
        address: '',
        phone,
        benefits: [`문의: ${otherPhone}`],
        lat: null,
        lng: null,
      });
      continue;
    }

    // 그 외 케이스는 스킵
  }
  return rows;
}

