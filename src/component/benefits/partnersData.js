function parseTSV(tsv) {
  const lines = tsv
    .split(/\r?\n/)
    .map(l => l.trim())
    .filter(Boolean);
  const rows = [];
  for (const line of lines) {
    const cols = line.split(/\t+/); // 탭 기준 분리
    if (!cols.length) continue;

    if (cols.length >= 4) {
      const [name, address, phone, benefitsRaw] = cols;
      rows.push({
        name,
        address,
        phone,
        benefits: (benefitsRaw || '')
          .split(/\s*[/•\-]\s*|\s{2,}/)
          .map(s => s.trim())
          .filter(Boolean),
        lat: null,
        lng: null,
      });
      continue; 
    }

    // 그 외 케이스는 스킵
  }
  return rows;
}

